public class adicao {
    private int n1;
    private int n2;

//Setters

    public void setNums1(int n1){
        this.n1 = n1;
}
    public void setNums2(int n2){
        this.n2 = n2;
    }
    public int getNums1(){
        return n1;
    }
    public int getNums2(){
        return n2;
    }
    public int soma(){
        return n1 + n2;
    }
}
