public class Div {
    private int n1;
    private int n2;
    
    public void setNum1(int n1){
        this.n1 = n1;
    }
    public void setNum2(int n2){
        this.n2 = n2;
    }
    public int getNum1(){
        return n1;
    }
    public int getNum2(){
        return n2;
    }
    public int div(){
        return n1 / n2;
    }
}
