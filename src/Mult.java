public class Mult {
    
    private int n1;
    private int n2;

    public void setNum1(int n1){
        this.n1 = n1;
    }

    public void setNum2(int n2){
        this.n2 = n2;
    }
    public int getMult1(){
        return n1;
    }
    public int getMult2(){
        return n2;
    }
    public int Multi(){
        return n1 * n2;
    }
}
